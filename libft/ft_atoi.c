int	ft_atoi(const char *s)
{
	int		i;
	int		sign;
	long	res;

	i = 0;
	sign = 1;
	res = 0;
	while (s[i] == '\t' || s[i] == '\n' || s[i] == '\v'
		|| s[i] == '\f' || s[i] == '\r' || s[i] == ' ')
		i++;
	if (s[i] == '-' || s[i] == '+')
		if (s[i++] == '-')
			sign = -sign;
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + s[i++] - '0';
		if (res < 0)
		{
			if (sign > 0)
				return (-1);
			return (0);
		}
	}
	return (res * sign);
}
